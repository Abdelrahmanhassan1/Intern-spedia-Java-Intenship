    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int index = jComboBox1.getSelectedIndex();
        double temp1, temp2;
        try{
            float temp = Float.parseFloat(jTextField1.getText());
            switch (index) {
                case 0:
                    // celsius
                    temp1 = (temp * (float)(9.0/5.0)) + 32;
                    temp2 = temp + (float)273.15;
                    
                    setLabels(temp, temp1, temp2);
                    break;
                case 1:
                    // fahrenheit
                    temp1 = (temp - 32) * (float)(5.0/9.0);
                    temp2 = (temp - 32) *(float)(5.0/9.0) + (float)273.15;
                    
                    setLabels(temp1, temp, temp2);
                    break;
                case 2:
                    // kelvin
                    temp1 = temp - (float)273.15;
                    temp2 = (temp - (float)273.15)  * (float)(9.0/5.0) + 32;                    
                    
                    setLabels(temp1, temp2, temp);
                    break;
                default:
                    break;
            }
        }catch(NumberFormatException e){
            showMessageDialog(null, "Enter Valid Value!");    
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

