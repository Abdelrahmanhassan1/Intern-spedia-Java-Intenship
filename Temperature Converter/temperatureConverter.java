    private void setLabels(double celsius, double fahrenheit, double kelvin)
    {
        celsius = Math.round(celsius * 100.0) / 100.0 ;
        fahrenheit = Math.round(fahrenheit * 100.0) / 100.0 ;
        kelvin = Math.round(kelvin * 100.0) / 100.0 ;
        
        jLabel6.setText(String.valueOf(celsius));
        jLabel7.setText(String.valueOf(fahrenheit));
        jLabel8.setText(String.valueOf(kelvin));
    }
